// ValidatePhoneNumber checks if a phone number is valid
func ValidatePhoneNumber(value string) bool {
	re := regexp.MustCompile(`^\+[0-9]{1,3}[0-9]{4,14}$`)
	return re.Match([]byte(value))
}
